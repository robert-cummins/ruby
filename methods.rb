def multiply(first_num, second_num)
    puts first_num.to_f * second_num.to_f
end

def addition(first_num, second_num)
    puts first_num.to_f + second_num.to_f
end

def subtract(first_num, second_num)
    puts first_num.to_f - second_num.to_f
end

def division(first_num, second_num)
    puts first_num.to_f / second_num.to_f
end

multiply(3, 5)
addition(2, 6)
subtract(8, 2)
division(10, 2)
